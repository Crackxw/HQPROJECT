//----------------------------------------------------------------------------------------------------------------
//	설명	:	생성자.
//----------------------------------------------------------------------------------------------------------------
OnlineObjectKI::OnlineObjectKI()
{
	ZeroMemory(szName, sizeof(szName));

	siFile			=	0;		// 화일 인덱스.
	siFont			=	0;		// 화일 폰트.
	siSprCenterX	=	0;		// 
	siSprCenterY	=	0;
	
	siXsize			=	0;		// 오브젝트가 차지 하는 X 타일
	siYsize			=	0;		// 오브젝트가 차지 하는 Y 타일

	siObjectAtb		=	0;		// 오브젝트 속성.

	siVillageKind	=	0;		// 마을의 종류.
		
	
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	소멸자.
//----------------------------------------------------------------------------------------------------------------
OnlineObjectKI::~OnlineObjectKI()
{
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	초기화.
//----------------------------------------------------------------------------------------------------------------
VOID	OnlineObjectKI::Init(CHAR *pszName, SI32 siKind, SI16 siFile, SI16 siFont, SI16 siXsize, SI16 siYsize, 
				 SI16 siSprCenterX, SI16 siSprCenterY, SI32 siObjectAtb, SI16 siVillageKind, SI16 siFrame, CHAR *pszMark)
{
	strcpy(szName, pszName);

	OnlineObjectKI::siKind			=	siKind;
	OnlineObjectKI::siFile			=	siFile;
	OnlineObjectKI::siFont			=	siFont;
	OnlineObjectKI::siXsize			=	siXsize;
	OnlineObjectKI::siYsize			=	siYsize;
	OnlineObjectKI::siSprCenterX	=	siSprCenterX;
	OnlineObjectKI::siSprCenterY	=	siSprCenterY;
	OnlineObjectKI::siObjectAtb		=	siObjectAtb;
	OnlineObjectKI::siVillageKind	=	siVillageKind;		
	OnlineObjectKI::siTotalFrame	=	siFrame;
	OnlineObjectKI::m_pszMark		=	pszMark;
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	이름을 구한다.
//----------------------------------------------------------------------------------------------------------------
VOID	OnlineObjectKI::GetName(CHAR pszName[32])
{
	strcpy(pszName, szName);
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	설명을 구한다.
//----------------------------------------------------------------------------------------------------------------
CHAR	*OnlineObjectKI::GetMark(VOID)
{
	return(m_pszMark);
}

