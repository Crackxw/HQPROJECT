///////////////////////////////////////////////////////////////////////////////////////////////////////
///	함수 :  VOID	InitLoadGameInitMenu()
/// 설명 : 
//////////////////////////////////////////////////////////////////////////////////////////////////////
VOID	InitLoadGameInitMenu()
{
	InitGameLoadMenu();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////
///	함수 :  SHORT	LoadGameInitMenu()
/// 설명 : 
//////////////////////////////////////////////////////////////////////////////////////////////////////
SHORT	LoadGameInitMenu()
{		
	SHORT	nReturn;

	nReturn = GameMenu();

	if(nReturn == GS_GAME)
	{
		FreeLoadGameInitMenu();
	}

	return nReturn;
}

VOID	FreeLoadGameInitMenu()
{
	FreeMenuBackgroundMusic();
}
