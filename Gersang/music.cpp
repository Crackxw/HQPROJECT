ENUM_EFFECT_TYPE EffectDataMgr::GetEnumType( char *pszStr )
{
	if( !pszStr )		return EFFECT_INVALID;
	for( register int i=EFFECT_FIRST; i<MAX_EFFECT_TYPE; i++ )	
	{
		if( strcmp( m_EffectData[i].m_pszString, pszStr ) == 0 ) return (ENUM_EFFECT_TYPE)i;
	}
	return EFFECT_INVALID;
}
#else
int EffectDataMgr::GetEnumType( char *pszStr )
{
	return GetMyHashTable()->Find(pszStr);
}
#endif

const char* EffectDataMgr::GetEnumType( int enIndex )
{
	if( enIndex <= EFFECT_FIRST || enIndex >= MAX_EFFECT_TYPE )		return NULL;
	return (const char*)(m_EffectData[enIndex].m_pszString);
}

BOOL EffectDataMgr::ClearBlankString(char const* pszCheckString, char* pszOutCleanBlankString)
{
	string szCheckName = pszCheckString;
	int nBlankStringIndex = szCheckName.find(" ");

	if( nBlankStringIndex > 0)
	{
		szCheckName.erase( nBlankStringIndex, szCheckName.size());
		strcpy(pszOutCleanBlankString, szCheckName.c_str());
		return FALSE;
	}
	return TRUE;
}

