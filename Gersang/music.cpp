// 버퍼에 저장된 소리를 하나 꺼내서 연주한다. 
void EffectDataMgr::Pop()
{
	if( m_EffectBufferIndex <= 0 )	
		return;
	
	// 버퍼에 저장된 것을 꺼낸다. 
	SHORT effect = m_EffectBuffer[0].Effect;
	SHORT x = m_EffectBuffer[0].X;
	SHORT y = m_EffectBuffer[0].Y;

	for(SHORT i=1; i<m_EffectBufferIndex; i++ )
	{
		m_EffectBuffer[i-1] = m_EffectBuffer[i];
	}
	m_EffectBufferIndex--;

	if( GameStatus == GS_GAME )
	{
		// 화면 중심과 주어진 좌표와의 상대적 거리를 구한다. 
		int sx=(x-Map.VirtualMapX);
		int sy=(y-Map.VirtualMapY);

		// 위치가 주어진 소리인데 거리가 멀면 소리내지 않는다. 
		if( x && y && (abs(sx)>17 || abs(sy)>17) )	return;
	}
	
	m_EffectData[effect].Play(x, y);
}


