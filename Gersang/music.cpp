void _EffectData::Free()
{
	// 모든 효과음을 다 해제한다. 
	for(SHORT i=0;i<EffectNumber;i++)
	{
		EffectID[i]=0;
	}
}

BOOL _EffectData::Play(SI32 x, SI32 y)	
{
	SHORT	index;
	SI32	i;
	BOOL	bPlay;
	SI32	listenerX, listenerY;

	if( g_pSoundManager == NULL )	return FALSE;

	// 준비된 효과음이 있다면, 
	if( EffectNumber == 0 )
	{
		clGrp.Error("No Sound", "No Sound");
		return TRUE;
	}

	/** EffectSound.txt 파일을 보면 공통된 Enum 값을 가진 값들이 있다. 
		해당 되는 Enum에 대한 사운드 파일을 플레이 할때 Enum 값들중에 공통된 것들이 있으면
		이 공통된것들 중에 하나만을 랜덤으로 플레이 하게 된다.
		EffectNumber는 해당되는 Enum 값에 대한 공통으로 있는 Enum들의 갯수이다.
	*/
	index = CurrentClock % EffectNumber;

	if( GameStatus == GS_GAME )
	{
		listenerX = Map.VirtualMapX,	listenerY = Map.VirtualMapY;
		// (x, y) 가 (0, 0)일 때는 3D사운드가 아니다.
		if( x < 0 || x >= Map.MH.MapXsize)			x = 0;
		if( y < 0 || y >= Map.MH.MapYsize)			y = 0;
	}
	else
	{
		listenerX = 0,	listenerY = 0;
	}

	if( x==0 && y==0 )	x = listenerX, y = listenerY;


	g_pSoundManager->SetCharPosition_Quarter(EffectID[index], x, y);	

	bPlay = TRUE;
	// Stop 속성이 0인 경우, 이 ID에 해당하는 효과음 중 하나라도 플레이 중이라면 플레이하지 않는다.
	if( g_pSoundManager->IsStop(EffectID[index]) == FALSE )
	{
		for( i=0; i<EffectNumber; i++ )
		{
			if( g_pSoundManager->IsPlaying(EffectID[i]) == TRUE )
			{
				bPlay = FALSE;
				break;
			}
		}
	}
	if( bPlay )
	{
		g_pSoundManager->SetListenerPosition_Quarter(listenerX, listenerY);	

		g_pSoundManager->PlaySound(EffectID[index]);
	}

	return TRUE;
}

