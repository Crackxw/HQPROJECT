#include "SONPCKI.h"

//----------------------------------------------------------------------------------------------------------------
//	설명	:	생성자.
//----------------------------------------------------------------------------------------------------------------
NPCKI::NPCKI()
{	
	uiID		=	0;
	siNation	=	0;
	siMoveSpeed	=	0;
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	소멸자.
//----------------------------------------------------------------------------------------------------------------
NPCKI::~NPCKI()
{
	Free();
}	

//----------------------------------------------------------------------------------------------------------------
//	설명	:	초기화.
//----------------------------------------------------------------------------------------------------------------
VOID	NPCKI::Init(UI16 uiid, SI16 sination, SI16 simovespeed)
{
	uiID		=	uiid;
	siNation	=	sination;
	siMoveSpeed	=	simovespeed;
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	해제
//----------------------------------------------------------------------------------------------------------------
VOID	NPCKI::Free()
{
	uiID		=	0;
	siNation	=	0;
}

//----------------------------------------------------------------------------------------------------------------
//	설명	:	ID를 얻어온다.
//----------------------------------------------------------------------------------------------------------------
UI16	NPCKI::GetID()
{
	return uiID;
}

