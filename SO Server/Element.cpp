#include <windows.h>
#include "Element.h"
//-----------------------------------------------------------------------------------------------------------------------
//	Name : CElement()
//	Desc : 생성자 
//-----------------------------------------------------------------------------------------------------------------------
CElement::CElement()
{
	m_key	 = -1;	
	m_uniqID = -1;
	m_bUsed  = FALSE;
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : ~CElement()
//	Desc : 소멸자 
//-----------------------------------------------------------------------------------------------------------------------
CElement::~CElement()
{
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : SetKey()
//	Desc : 원소에 키를 할당한다 
//-----------------------------------------------------------------------------------------------------------------------
void CElement::SetKey(int key)	
{	
	m_key = key;	
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : GetKey()
//	Desc : 원소에 지정된 키를 얻는다 
//-----------------------------------------------------------------------------------------------------------------------
int CElement::GetKey()
{		
	return m_key;	
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : SetUsed()
//	Desc : 원소의 사용여부를 결정한다 
//-----------------------------------------------------------------------------------------------------------------------
void CElement::SetUsed(bool which)	
{
	m_bUsed = which;	
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : GetUsed()
//	Desc : 원소의 현재 사용여부를 얻는다 
//-----------------------------------------------------------------------------------------------------------------------
bool CElement::GetUsed()  
{
	return m_bUsed;
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : SetUniqID()
//	Desc : 유일한 아이디 값을 할당한다 
//-----------------------------------------------------------------------------------------------------------------------
void CElement::SetUniqID(DWORD uniqID)	
{
	m_uniqID = uniqID;	
}



//-----------------------------------------------------------------------------------------------------------------------
//	Name : GetUniqID()
//	Desc : 유일한 아이디 값을 얻는다 
//-----------------------------------------------------------------------------------------------------------------------
DWORD CElement::GetUniqID()  
{
	return m_uniqID;
}