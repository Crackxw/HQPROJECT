CSkillMgr::CSkillMgr(SOPlayer* owner)
{
	// 각 스킬 생성 
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : ~CSKillMgr()
//	Desc : 
//-----------------------------------------------------------------------------------------------------------------------
CSkillMgr::~CSkillMgr()
{
	// 각 스킬 해제 
	for(int i=0; i < MAX_SKILL; ++i)
	{
		if( m_SkillList[i] != NULL ) delete m_SkillList[i];		
	}
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : getSkill()
//	Desc : 해당하는 스킬을 얻어온다 
//-----------------------------------------------------------------------------------------------------------------------
CSkill* CSkillMgr::getSkill(int which)
{
	for(int i=0; i < MAX_SKILL; ++i)
	{		
		if( which == m_SkillList[i]->getKind() ) return m_SkillList[i];
	}

	return NULL;
}