//-----------------------------------------------------------------------------------------------------------------------
//	Name : CSkill()
//	Desc : 
//-----------------------------------------------------------------------------------------------------------------------
CSkill::CSkill(int kind, int maxLevel): m_kind( kind ), m_maxLevel( maxLevel )
{	
	m_curLevel = 0;	
	m_bUsed	   = FALSE;
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : ~CSkill()
//	Desc : 
//-----------------------------------------------------------------------------------------------------------------------
CSkill::~CSkill()
{
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : getKind()
//	Desc : 이 스킬의 종류를 리턴한다 
//-----------------------------------------------------------------------------------------------------------------------
int CSkill::getKind() 
{
	return m_kind;
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : isLearnSkill()
//	Desc : 이 스킬을 익혔는가?
//-----------------------------------------------------------------------------------------------------------------------
bool CSkill::isLearnSkill()
{
	return m_curLevel > 0 ? TRUE : FALSE;
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : LevelUp()
//	Desc : 이 스킬을 한단계 레벨업 시킨다 
//-----------------------------------------------------------------------------------------------------------------------
int CSkill::LevelUp()
{
	if( ++m_curLevel > m_maxLevel ) m_curLevel = m_maxLevel;

	return m_curLevel;
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : setCurLevel()
//	Desc : 현재 이 스킬의 레벨을 설정한다  
//-----------------------------------------------------------------------------------------------------------------------
void CSkill::setCurLevel(int value)
{
	m_curLevel = value;	
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : setUsed()
//	Desc : 
//-----------------------------------------------------------------------------------------------------------------------
void CSkill::setUsed(bool which)
{
	m_bUsed = which;
}


//-----------------------------------------------------------------------------------------------------------------------
//	Name : getUsed()
//	Desc : 
//-----------------------------------------------------------------------------------------------------------------------
bool CSkill::getUsed()
{
	return m_bUsed;
}
